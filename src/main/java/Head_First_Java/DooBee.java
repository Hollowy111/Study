package Head_First_Java;

public class DooBee {

    public static void main(String[] args) {
        int x = 0;
        while (x < 2) {
            System.out.println("Doo");
            System.out.println("Bee");
            x = x + 1;
        }
        if (x == 0) {
            System.out.println("Do");
        }
    }
}
